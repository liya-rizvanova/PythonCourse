b = {2, 5, 8, 13, 21}
c = a.copy()   # c = {1, 2, 3, 5, 8}
u = a.union(b)   # u = {1, 2, 3, 5, 8, 13, 21} - —Å–æ–∑–¥–∞–µ—Ç —Å–ø–∏—Å–æ–∫ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤ a –∏ b
i = a.intersection(b)   # i = {8, 2, 5} - –≤—ã–±–∏—Ä–∞–µ—Ç –ø–µ—Ä–µ—Å–µ–∫–∞—é—â–∏–µ—Å—è —ç–ª–µ–º–µ–Ω—Ç—ã –æ–±–æ–∏—Ö –º–Ω–æ–∂–µ—Å—Ç–≤ a –∏ b
dl = a.difference(b)   # dl = {1, 3} - –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ a –≤—ã—á–∏—Ç–∞–µ–º –≤—Å–µ –∑–Ω–∞—á–µ–Ω–∏—è, —Å–æ–¥–µ—Ä–∂–∞—â–∏–µ—Å—è –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ b
dr = b.difference(a)   # dr = {13, 21} - –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ b –≤—ã—á–∏—Ç–∞–µ–º –≤—Å–µ –∑–Ω–∞—á–µ–Ω–∏—è, —Å–æ–¥–µ—Ä–∂–∞—â–∏–µ—Å—è –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ a
q=a.union(b).difference(a.intersection(b))   # {1, 21, 3, 13} - a.intersection(b) –ø–µ—Ä–≤–æ–µ –¥–µ–π—Å—Ç–≤–∏–µ, –∞ –¥–∞–ª–µ–µ a.union(b).difference()

# –ù–µ–∏–∑–º–µ–Ω—è–µ–º–æ–µ –∏–ª–∏ –∑–∞–º–æ—Ä–æ–∂–µ–Ω–Ω–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ(frozenset) ‚Äî –º–Ω–æ–∂–µ—Å—Ç–≤–æ, —Å –∫–æ—Ç–æ—Ä—ã–º –Ω–µ –±—É–¥—É—Ç —Ä–∞–±–æ—Ç–∞—Ç—å –º–µ—Ç–æ–¥—ã —É–¥–∞–ª–µ–Ω–∏—è –∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è.
a = {1, 2, 3, 5, 8}
b = frozenset(a)
print(b) # frozenset({1, 2, 3, 5, 8})

'''
List Comprehension ¬´–≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞ —Å–ø–∏—Å–∫–∞¬ª. 
List comprehension ‚Äî —ç—Ç–æ —É–ø—Ä–æ—â–µ–Ω–Ω—ã–π –ø–æ–¥—Ö–æ–¥ –∫ —Å–æ–∑–¥–∞–Ω–∏—é —Å–ø–∏—Å–∫–∞, –∫–æ—Ç–æ—Ä—ã–π –∑–∞–¥–µ–π—Å—Ç–≤—É–µ—Ç —Ü–∏–∫–ª for, 
–∞ —Ç–∞–∫–∂–µ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ if-else –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Ç–æ–≥–æ, —á—Ç–æ –≤ –∏—Ç–æ–≥–µ –æ–∫–∞–∂–µ—Ç—Å—è –≤ —Ñ–∏–Ω–∞–ª—å–Ω–æ–º —Å–ø–∏—Å–∫–µ.

1. –ü—Ä–æ—Å—Ç–∞—è —Å–∏—Ç—É–∞—Ü–∏—è ‚Äî —Å–ø–∏—Å–æ–∫:
list_1 = [exp for item in iterable]
1. –í—ã–±–æ—Ä–∫–∞ –ø–æ –∑–∞–¥–∞–Ω–Ω–æ–º—É —É—Å–ª–æ–≤–∏—é:
list_1 = [exp for item in iterable (if conditional)]
'''
# –ó–∞–¥–∞—á–∞: –°–æ–∑–¥–∞—Ç—å —Å–ø–∏—Å–æ–∫, —Å–æ—Å—Ç–æ—è—â–∏–π –∏–∑ —á–µ—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ –æ—Ç 1 –¥–æ 100.
# –†–µ—à–µ–Ω–∏–µ:
# 1. –°–æ–∑–¥–∞—Ç—å —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 100
list_1 = []
for i in range(1, 101):
    list_1.append(i)
print(list_1)   # [1, 2, 3,..., 100]

# –≠—Ç—É –∂–µ —Ñ—É–Ω–∫—Ü–∏—é –º–æ–∂–Ω–æ –∑–∞–ø–∏—Å–∞—Ç—å —Ç–∞–∫:
list_1 = [i for i in range(1, 101)]   # [1, 2, 3,..., 100]

# 2. –î–æ–±–∞–≤–∏—Ç—å —É—Å–ª–æ–≤–∏–µ (—Ç–æ–ª—å–∫–æ —á—ë—Ç–Ω—ã–µ —á–∏—Å–ª–∞)
list_1 = [i for i in range(1, 101) if i % 2 == 0]   # [2, 4, 6,..., 100]

# –î–æ–ø—É—Å—Ç–∏–º, –≤—ã —Ä–µ—à–∏–ª–∏ —Å–æ–∑–¥–∞—Ç—å –ø–∞—Ä—ã –∫–∞–∂–¥–æ–º—É –∏–∑ —á–∏—Å–µ–ª (–∫–æ—Ä—Ç–µ–∂–∏)
list_1 = [(i, i) for i in range(1, 101) if i % 2 == 0]   # [(2, 2), (4, 4),...,(100, 100)]

# –¢–∞–∫–∂–µ –º–æ–∂–Ω–æ —É–º–Ω–æ–∂–∞—Ç—å, –¥–µ–ª–∏—Ç—å, –ø—Ä–∏–±–∞–≤–ª—è—Ç—å, –≤—ã—á–∏—Ç–∞—Ç—å. –ù–ø—Ä., —É–º–Ω–æ–∂–∏—Ç—å –∑–Ω–∞—á–µ–Ω–∏–µ –Ω–∞ 2.
list_1 = [i * 2 for i in range(10) if i % 2 == 0]
print(list_1)   # [0, 4, 8, 12, 16]

