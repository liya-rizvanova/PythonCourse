'''
lambda  - —ç—Ç–æ –∫–ª—é—á–µ–≤–æ–µ —Å–ª–æ–≤–æ –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –∞–Ω–æ–Ω–∏–º–Ω—ã—Ö —Ñ—É–Ω–∫—Ü–∏–π (—Ñ—É–Ω–∫—Ü–∏–π –±–µ–∑ –∏–º–µ–Ω–∏). 
–û–Ω–∏ –æ–±—ã—á–Ω–æ –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è –≤ —Ç–µ—Ö —Å–ª—É—á–∞—è—Ö, –∫–æ–≥–¥–∞ –Ω—É–∂–Ω–∞ –Ω–µ–±–æ–ª—å—à–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –∫–æ—Ä–æ—Ç–∫–∏—Ö –æ–ø–µ—Ä–∞—Ü–∏–π. 
lambda –º–æ–∂–µ—Ç –ø—Ä–∏–Ω–∏–º–∞—Ç—å –ª—é–±–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤, –Ω–æ —Å–æ–¥–µ—Ä–∂–∏—Ç —Ç–æ–ª—å–∫–æ –æ–¥–Ω–æ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ.
'''
def calc(op, a, b):
    print(op(a, b))

def sum(x, y):
    return x + y
calc(sum, 4, 5)
# ‚áî (—Ä–∞–≤–Ω–æ—Å–∏–ª—å–Ω–æ)
