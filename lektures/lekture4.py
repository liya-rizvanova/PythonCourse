calc(sum, 4, 5)
# ‚áî (—Ä–∞–≤–Ω–æ—Å–∏–ª—å–Ω–æ)
calc(lambda x, y: x + y, 4, 5)   # 9

'''–ó–∞–¥–∞—á–∞.
–í —Å–ø–∏—Å–∫–µ —Ö—Ä–∞–Ω—è—Ç—Å—è —á–∏—Å–ª–∞. –ù—É–∂–Ω–æ –≤—ã–±—Ä–∞—Ç—å —Ç–æ–ª—å–∫–æ —á—ë—Ç–Ω—ã–µ —á–∏—Å–ª–∞ –∏ —Å–æ—Å—Ç–∞–≤–∏—Ç—å —Å–ø–∏—Å–æ–∫ –ø–∞—Ä (—á–∏—Å–ª–æ; –∫–≤–∞–¥—Ä–∞—Ç —á–∏—Å–ª–∞).
–ü—Ä–∏–º–µ—Ä: 1 2 3 5 8 15 23 38
–ü–æ–ª—É—á–∏—Ç—å: [(2, 4), (8, 64), (38, 1444)]
–†–µ—à–µ–Ω–∏–µ:
'''
data = [1, 2, 3, 5, 8, 15, 23, 38]
out = []
for i in data :
    if i % 2 == 0:
        out.append((i, i ** 2))
print(out)

def select(f, col):
    return [f(x) for x in col]
def where(f, col):
    return [x for x in col if f(x)]
data = [1, 2, 3, 5, 8, 15, 23, 38]
res = select(int, data)
print(res)
res = where(lambda x: x % 2 == 0, res)
print(res)   # [2, 8, 38]
res = list(select(lambda x: (x, x ** 2), res))
print(res)

#  map() –ø–æ–∑–≤–æ–ª–∏—Ç –∏–∑–±–∞–≤–∏—Ç—å—Å—è –æ—Ç —Ñ—É–Ω–∫—Ü–∏–∏ select.
def where(f, col):
    return [x for x in col if f(x)]
data = '1 2 3 5 8 15 23 38'.split()
res = map(int, data)
res = where(lambda x: x % 2 == 0, res)
res = list(map(lambda x: (x, x ** 2), res))
print(res)

# filter() –ø–æ–∑–≤–æ–ª–∏—Ç –∏–∑–±–∞–≤–∏—Ç—å—Å—è –æ—Ç —Ñ—É–Ω–∫—Ü–∏–∏ where
data = '1 2 3 5 8 15 23 38'.split()
