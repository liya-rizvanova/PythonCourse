def max_berry_count(arr):
    arr_count = []

    for i in range(len(arr)):
        if i >= 2:
            arr_count.append(arr[i-2] + arr[i-1] + arr[i])

    return max(arr_count) if arr_count else 0

# Пример использования
arr = [5, 8, 6, 4, 9, 2, 7, 3]
result = max_berry_count(arr)
print(result)
